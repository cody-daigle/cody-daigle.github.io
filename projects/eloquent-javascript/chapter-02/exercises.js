function drawChessboard(size) {
  //empty string for chessboard
let chess = '';
//first loop
for (let i = 0; i < size; i++) {
  //second loop
  for (let e = 0; e < size; e++) {
    //check if i + e is even
    if ((e + i) % 2 == 0) {
      //if it is then add a space
      chess += ' ';
      //otherwise place a #
    } else {
      chess += '#';
    }
  }
  // add a line break after each set
  chess += '\n';
}
console.log(chess)
//return chess
}
