// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step){
  let output = [];
  if(step === undefined){// || step === 0
    if(start < end){
    for(let i = start; i <= end; i++){
      output.push(i)
      }
    }else{
      for(let i = start; i >= end; i--){
        output.push(i)
      }
    }
  }else{
    if(step > 0){
      for(let i = start; i <= end; i += step){
        output.push(i);
      }
    }else{
      for(let i = start; i >= end; i += step){
        output.push(i);
      }
    }
  }
  return output;
}



