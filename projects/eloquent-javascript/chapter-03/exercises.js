// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if(num1 < num2){
    return num1
  }
  return num2
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
  if(n % 2 === 0){
    return true;
  }else if(n % 2 !== 0){
    return false;
  }
  return null;
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, x) {
  let count = 0;
//loop through string
for(let i = 0; i < str.length; i++){
  if(str[i] === x){ //hard coding letters will fail...
    count += 1;
  }
}
return count

}
